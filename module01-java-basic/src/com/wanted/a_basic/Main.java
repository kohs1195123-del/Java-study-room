package com.wanted.a_basic;

public class Main {

    /*
    * Java 의 기본 구조 및 실행 방식
    * 모든 Java 기반의 프로그램은 클래스 안에서 동작한다.!!
    * main() 메서드가 포르그램의 시작점이 된다.
    *
    * Java 의 실헹 과정
    * 1. Java 소스코드 (.Java) 를 작성한다.
    * 2. 컴파일러(Javac) 를 사용하여 바이트코드(.class) 로 변횐힌디.
    * 3. JVM ( Java virtual Machine) 이 .class 파일을 실행한다.
    * 4. JVM 은 바이트코드 해석하여 OS 에서 실행 가능한 기계어로 변환
    * 5. 실행 결과를 출력하거나, 다른 연산을 진행한다.
    *
    * 주요 개념
    *  컴파일(compile) : .Java -> .class 변환 ( JVM 이 이해할 수 있도록)
    * -바이트코드(Bytecode) : JVM에서 실행 가능한 중간 코드
    * -JVM : 바이트 코드를  os 에 맞는 기계어로 변환하여 실행
    *
     */
    public static void main(String[] args) {
        System.out.println("프로그래밍 세상 온 것을 환영합니다.");

    }

}